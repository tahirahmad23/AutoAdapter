class AXISTrafficGenerator:
    """Generates randomized AXI4-Stream traffic with correct TUSER metadata."""

    def __init__(self, dut, data_width=512, tuser_width=64):
        self.dut = dut
        self.data_width = data_width
        self.tuser_width = tuser_width
        self.data_bytes = data_width // 8
        self.tuser_bytes = tuser_width // 8

    def random_packet(self, min_size=64, max_size=9216):
        pkt_len = random.randrange(min_size, max_size + 1)
        num_beats = (pkt_len + self.data_bytes - 1) // self.data_bytes
        tdata = []
        tkeep = []
        for i in range(num_beats):
            if i < num_beats - 1:
                beat_data = bytes(random.getrandbits(8) for _ in range(self.data_bytes))
                tdata.append(int.from_bytes(beat_data, "little"))
                tkeep.append((1 << self.data_bytes) - 1)
            else:
                last_bytes = pkt_len - i * self.data_bytes
                beat_data = bytes(random.getrandbits(8) for _ in range(last_bytes))
                padded = beat_data + b"\\x00" * (self.data_bytes - last_bytes)
                tdata.append(int.from_bytes(padded, "little"))
                tkeep.append((1 << last_bytes) - 1)
        return tdata, tkeep, pkt_len

    def random_tuser(self, fields_info):
        tuser = 0
        for fname, offset, width in fields_info:
            value = random.getrandbits(width)
            tuser |= (value & ((1 << width) - 1)) << offset
        return tuser


